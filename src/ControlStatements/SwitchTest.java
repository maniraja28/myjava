package ControlStatements;

public class SwitchTest {

	public static void main(String[] args) {
		int x = 1;
		switch (x) {
		case 1:
			System.out.println("1");
		case 2:
			System.out.println("2");

		case 3:
			System.out.println("3");
		case 4:
			System.out.println("4");
		default:
			System.out.println("default");

		}
	}

}
